//1.为什么要有动态内存分配？
int main()
{
	int a = 10;//申请4个字节
	char c = 'w';//申请1个字节
	int arr[30] = { 0 };//申请40个字节，但是申请好后，空间大小不能调整
	//变长数组只是说数组的大小可以使用变量来指定，而一旦数组创建好后，依然不能调整大小
	//申请的空间大小不能灵活的调整
	//C语言就给了：动态内存管理，给与程序员权限，自己申请，自己使用，使用之后，自己释放
	return 0;
}